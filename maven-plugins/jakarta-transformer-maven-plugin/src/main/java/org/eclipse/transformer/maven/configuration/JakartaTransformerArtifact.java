package org.eclipse.transformer.maven.configuration;

import com.alipay.sofa.migration.util.AcrLogUtil;

import java.util.List;

/**
 * The input artifact to transform jakarta version.
 *
 * @author yuanxuan
 * @version : JakartaTransformerArtifact.java, v 0.1 2023年06月29日 14:42 yuanxuan Exp $
 */
public class JakartaTransformerArtifact {

	private String       groupId;
	private String       artifactId;
	private String       version;
	private String       type;
	private String       classifier;
	private List<String> excludes;




	private boolean skipSource;
	private boolean skipJavadoc;

	public String getGroupId() {
		return groupId;
	}

	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}

	public String getArtifactId() {
		return artifactId;
	}

	public void setArtifactId(String artifactId) {
		this.artifactId = artifactId;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getClassifier() {
		return classifier;
	}

	public void setClassifier(String classifier) {
		this.classifier = classifier;
	}

	public List<String> getExcludes() {
		return excludes;
	}

	public void setExcludes(List<String> excludes) {
		this.excludes = excludes;
	}
}
